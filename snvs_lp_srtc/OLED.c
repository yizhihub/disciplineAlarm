void OLED_WrDat(unsigned char dat)
{
  unsigned char i=8;
  OLED_CS_0;
  OLED_DC_1;
 // NOP();NOP();NOP();NOP(); // yanshi
  OLED_SCL_0;;
 // NOP();NOP();NOP();NOP(); // yanshi 
  while(i--)
  {
    if(dat&0x80)
	  {OLED_SDA_1;}
    else
	  {OLED_SDA_0;}
	//NOP();NOP();// yanshi  
    OLED_SCL_1;
  //  NOP();NOP();// yanshi             
    OLED_SCL_0;   
  //  NOP();NOP();// yanshi     
    dat<<=1;    
  }
  OLED_CS_1;
}
void OLED_WrCmd(unsigned char cmd)
{
  unsigned char i;
  OLED_CS_0;
  OLED_DC_0;;
 // NOP();NOP();NOP();NOP(); // yanshi
  OLED_SCL_0;;
 // NOP();NOP();NOP();NOP();// yanshi
  for(i=0;i<8;i++)
  {
   if(cmd&0x80)
	  OLED_SDA_1;
   else
	  OLED_SDA_0;
  // NOP();NOP();//yanshi 
   OLED_SCL_1;
  // NOP();NOP();// yanshi   
   OLED_SCL_0;
  // NOP();NOP();// yanshi  
   cmd<<=1;;  
  } 	
  OLED_CS_1;
}
void OLED_Set_Pos(unsigned char x, unsigned char y)
{ 
  #ifdef CHIP_SH1106
  x += 2;
  #endif
  OLED_WrCmd(0xb0+y);
  OLED_WrCmd(x&0x0f);
  OLED_WrCmd(((x&0xf0)>>4)|0x10);
  //OLED_WrCmd((x&0x0f)|0x01); 
  // 这个错误会导致 OLED_P8x16Char(120,4,95) 函数会在下一列多写一竖线
} 
void OLED_PutPixel(uint8_t x,uint8_t y)
{
    
    OLED_WrCmd(0xb0+7-y/8);   //0xb0+0~7表示页0~页7?
    #ifdef CHIP_SH1106    //0x00+0~16表示将128列分成16组其地址在某组中的第几列
    OLED_WrCmd(0x02 + x%16);
    #else 
    OLED_WrCmd(0x00 + x%16);
    #endif  
    OLED_WrCmd(0x10 + x/16);     //0x10+0~16表示将128列分成16组其地址所在第几组
    OLED_WrDat(0x80>>(y%8));
}
void OLED_Fill(unsigned char bmp_dat)
{
	unsigned char y,x;
	
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
        #ifdef CHIP_SH1106
        OLED_WrCmd(0x02);
        #else 
		OLED_WrCmd(0x00);
        #endif
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			OLED_WrDat(bmp_dat);
	}
}
void OLED_CLS(void)
{
	unsigned char y,x;	
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
        #ifdef CHIP_SH1106
        OLED_WrCmd(0x02);
        #else 
		OLED_WrCmd(0x00);
        #endif
		OLED_WrCmd(0x10); 
		for(x=0;x<X_WIDTH;x++)
			OLED_WrDat(0);
	}
}

void OLED_Init(void)        
{  
    OLED_SCL_1;
    //OLED_CS=1;	//预制SLK和SS为高电平  	

    msDelay(1);
    OLED_RST_0;
    msDelay(1);
    OLED_RST_1;

    //从上电到下面开始初始化要有足够的时间，即等待RC复位完毕   

    OLED_WrCmd(0xae);//--turn off oled panel
    OLED_WrCmd(0x00);//---set low column address,notice the start column is 0x02 when it's SH1106,but don't care here.
    OLED_WrCmd(0x10);//---set high column address
    OLED_WrCmd(0x40);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
    OLED_WrCmd(0xb0);//set page address
    OLED_WrCmd(0x81);//--set contrast control register
    OLED_WrCmd(0xF0); // Set SEG Output Current Brightness
    OLED_WrCmd(0xa0);//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
    OLED_WrCmd(0xc0);//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
    OLED_WrCmd(0xa6);//--set normal display
    OLED_WrCmd(0xa8);//--set multiplex ratio(1 to 64)
    OLED_WrCmd(0x3f);//--1/64 duty
    OLED_WrCmd(0xd3);//-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
    OLED_WrCmd(0x00);//-not offset
    OLED_WrCmd(0xd5);//--set display clock divide ratio/oscillator frequency
    OLED_WrCmd(0x80);//--set divide ratio, Set Clock as 100 Frames/Sec
    OLED_WrCmd(0xd9);//--set pre-charge period
    OLED_WrCmd(0xf1);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
    OLED_WrCmd(0xda);//--set com pins hardware configuration
    OLED_WrCmd(0x12);
    OLED_WrCmd(0xdb);//--set vcomh
    OLED_WrCmd(0x40);//Set VCOM Deselect Level

    /*
    * there are diffierent register config between SSD1306 and SH1106
    */
    #ifdef CHIP_SH1106
    OLED_WrCmd(0xad);
    OLED_WrCmd(0x8b);
    OLED_WrCmd(0x33);
    #endif
    //OLED_WrCmd(0x20);//-Set Page Addressing Mode (0x00/0x01/0x02)
    //OLED_WrCmd(0x02);//
    //OLED_WrCmd(0xa4);// Disable Entire Display On (0xa4/0xa5)
    //OLED_WrCmd(0xa6);// Disable Inverse Display On (0xa6/a7) 
    OLED_WrCmd(0x8d);//--set Charge Pump enable/disable
    OLED_WrCmd(0x14);//--set(0x10) disable
    OLED_WrCmd(0xaf);//--turn on oled panel
    OLED_Fill(0x00);  //初始清屏
    OLED_Set_Pos(0,0); 	
} 


